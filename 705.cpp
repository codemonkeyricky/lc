    MyHashSet()
    {
        hash = vector<int>(1000001);
    }

    void add(int key)
    {
        ++hash[key];
    }

    void remove(int key)
    {
        hash[key] = 0;
    }

    bool contains(int key)
    {
        return hash[key];
    }

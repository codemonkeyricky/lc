struct TreeNode
{
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

TreeNode *recurse(vector<int> &tree, int k)
{
    if (k >= tree.size() || tree[k] == -1)
        return nullptr;

    return new TreeNode(tree[k], recurse(tree, k * 2 + 1), recurse(tree, k * 2 + 2));
}

TreeNode *populate(vector<int> &tree)
{
    return recurse(tree, 0);
}

class Solution
{
public:
    vector<double> sampleStats(vector<int> &count)
    {
        int n = count.size();
        vector<double> rv;
        for (auto i = 0; i < n; ++i)
            if (count[i])
            {
                rv.push_back(i);
                break;
            }

        for (int i = n - 1; i >= 0; --i)
            if (count[i])
            {
                rv.push_back(i);
                break;
            }

        long sum = 0, c = 0;
        for (auto i = 0; i < n; ++i)
            sum += count[i] * i, c += count[i];
        rv.push_back((double)sum / c);

        auto copy = count;
        int i = 0, j = n - 1;
        double kk = 0;
        while (i < j)
        {
            while (i < j && !count[i])
                ++i;

            while (i < j && !count[j])
                --j;

            if (i == j)
                break;

            int mm = min(count[i], count[j]);
            count[i] -= mm, count[j] -= mm;

            if (!count[i] && !count[j])
            {
                kk = ((double)i + j) / 2;
                ++i, --j;
            }
            else if (!count[i])
            {
                kk = j;
                ++i;
            }
            else if (!count[j])
            {
                kk = i;
                --j;
            }
        }
        count = copy;
        rv.push_back(kk);

        int k = 0;
        for (auto i = 0; i < n; ++i)
            if (count[i] > count[k])
                k = i;
        rv.push_back(k);

        return rv;
    }
};


/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
  public:
    vector<int> rightSideView(TreeNode* root) {
        vector<int> rv;
        queue<TreeNode*> q;

        if (root)
            q.push(root);
        while (q.size()) {

            auto s = q.size();
            while (s) {

                auto n = q.front();
                q.pop();

                if (n->left)
                    q.push(n->left);
                if (n->right)
                    q.push(n->right);

                --s;
                if (s == 0) {
                    rv.push_back(n->val);
                }
            }
        }

        return rv;
    }
};

